public class Derslik {
    private int id;
    private String bina_Adi;
    private String salon_No;
    private String salon_Kodu;
    private int salon_Kapasitesi;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBina_Adi() {
        return bina_Adi;
    }

    public void setBina_Adi(String bina_Adi) {
        this.bina_Adi = bina_Adi;
    }

    public String getSalon_No() {
        return salon_No;
    }

    public void setSalon_No(String salon_No) {
        this.salon_No = salon_No;
    }

    public String getSalon_Kodu() {
        return salon_Kodu;
    }

    public void setSalon_Kodu(String salon_Kodu) {
        this.salon_Kodu = salon_Kodu;
    }

    public int getSalon_Kapasitesi() {
        return salon_Kapasitesi;
    }

    public void setSalon_Kapasitesi(int salon_Kapasitesi) {
        this.salon_Kapasitesi = salon_Kapasitesi;
    }
    
    
}
